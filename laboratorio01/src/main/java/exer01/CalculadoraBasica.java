package exer01;

public class CalculadoraBasica {
	
	public int soma(int a, int b) {
		return a + b;
	}
	
	public int divisao(int a, int b) {
		return a / b;
	}
	
	public int subtracao(int a, int b) {
		return a - b;
	}	

	public int multiplicao(int a, int b) {
		return a * b;
	}
	
	public int modulo(int a, int b) {
		return a % b;
	}	
}
